template <typename T_DATA> class base16_algo
{
  protected:
    T_DATA data;

  public:
    error encode(const std::string in, std::string& out) const
    {
        error          err;
        const uint8_t* enctab = data.get_enctab();

        size_t len = in.length();
        out.reserve(2 * len);
        out = "";
        for (size_t i = 0; i < len; i++) {
            out += enctab[(in[i] & 0xF0) >> 4];
            out += enctab[in[i] & 0x0F];
        }

        return err;
    };

    error decode(const std::string in, std::string& out) const
    {
        error          err;
        const uint8_t* dectab = data.get_dectab();

        size_t len = in.length();
        if (len % 2 == 1) {
            err = "Invalid input length";
            return err;
        }

        out.reserve(len / 2);
        out = "";
        for (size_t i = 0; i < len; i += 2) {
            if (dectab[in[i]] == 0xFF || dectab[in[i + 1]] == 0xFF) {
                err = "Invalid character found in the input";
                return err;
            }
            out += (dectab[in[i + 0]] << 4) | dectab[in[i + 1]];
        }

        return err;
    };
};

class base16 : public base16_algo<base16_data>
{
};

